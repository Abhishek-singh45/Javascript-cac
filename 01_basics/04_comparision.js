//COMPARISIONS.

console.log(2 > 1); //true
console.log(2 >= 1); //true
console.log(2 < 1); //false
console.log(2 == 1); //false
console.log(2 != 1); //true

console.log("2" > 1); // automatically string change into a number they give output is true.

// not predictable output
console.log(null > 0);  // false
console.log(null == 0); // false 
console.log(null >= 0); // true 
console.log(undefined > 0);  // false
console.log(undefined == 0); // false 
console.log(undefined >= 0); // false 

//strict comparision -> ===

console.log("2" === 2); //false because data type different  

